import math

#math functions
x=4.5
y=4.3

a=10
b=3

#ceil()
print(math.ceil(x))
print(math.ceil(y))

#floor()
print(math.floor(x))
print(math.floor(y))

#f mod()
print(math.fmod(a,b))
print(math.fmod(x,y))

#exp()
print(math.exp(x))
print(math.exp(y))

#factorial()
print(math.factorial(a))
print(math.factorial(b))

#sqrt()
print(math.sqrt(a))
print(math.sqrt(b))

#pow()=power
print(math.pow(a,b))
print(math.pow(b,a))

#math constants

#pi
print(math.pi)

#e
print(math.e)
